    private void languageChangerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_languageChangerActionPerformed
        // TODO add your handling code here:
        if (flag == false) {
            label1.setText("English");
            label2.setText("Azerbaijani");
            buttonSpeaker1.setEnabled(true);
            buttonSpeaker2.setEnabled(false);
            flag = true;
        } else {
            label1.setText("Azerbaijani");
            label2.setText("English");
            buttonSpeaker2.setEnabled(true);
            buttonSpeaker1.setEnabled(false);
            flag = false;
        }
        String temp = textArea1.getText();
        textArea1.setText("");
        textArea1.setText(textArea2.getText());
        textArea2.setText(temp);
        listModel.removeAllElements();
    }//GEN-LAST:event_languageChangerActionPerformed

    private void checkBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkBoxActionPerformed
        // TODO add your handling code here:
        if (checkBox.isSelected()) {
            buttonTranslate.setEnabled(true);
            flag2 = false;
            listModel.removeAllElements();
            jlist.setModel(listModel);
            jlist.setVisible(false);
        } else {
            buttonTranslate.setEnabled(false);
            jlist.setVisible(true);
            flag2 = true;
            searchInWordList();
        }
    }//GEN-LAST:event_checkBoxActionPerformed

