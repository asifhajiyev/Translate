    private void searchInWordList() {
        // Word word = list.stream().filter(w -> textField1.getText().equals(w.getWordAZ())).findAny().orElse(null);
        listModel.removeAllElements();
        jlist.setModel(listModel);
        if (label1.getText().equals("Azerbaijani")) {
            list.stream().filter((w) -> (w.getWordAZ().toLowerCase().startsWith(textArea1.getText().trim().toLowerCase()))).forEachOrdered((w) -> {
                if (!listModel.contains(w.getWordAZ().toLowerCase())) {
                    listModel.addElement(w.getWordAZ().toLowerCase());
                }
                jlist.setModel(listModel);
            });

            if (textArea1.getText().equals("")) {
                textArea2.setText("");
                listModel.removeAllElements();
            }
        } else {
            list.stream().filter((w) -> (w.getWordEN().toLowerCase().startsWith(textArea1.getText().trim().toLowerCase()))).forEachOrdered((w) -> {
                if (!listModel.contains(w.getWordEN().toLowerCase())) {
                    listModel.addElement(w.getWordEN().toLowerCase());
                }
                jlist.setModel(listModel);
            });
            if (textArea1.getText().equals("")) {
                textArea2.setText("");
                listModel.removeAllElements();
            }
        }
    }

