    private void jlistMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlistMouseClicked
        // TODO add your handling code here:
        textArea2.setText("");
        String value = jlist.getSelectedValue();
        textArea1.setText(value);
        if (label1.getText().equals("English")) {
            ArrayList<Word> traList = new ArrayList<>();
            traList = wordDatabaseManager.getWord_AZ(value);
            for (Word word : traList) {
                textArea2.append(word.getWordAZ() + "\n");
            }
        } else if (label1.getText().equals("Azerbaijani")) {
            ArrayList<Word> traList = new ArrayList<>();
            traList = wordDatabaseManager.getWord_EN(value);
            for (Word word : traList) {
                textArea2.append(word.getWordEN() + "\n");
            }
        }
    }//GEN-LAST:event_jlistMouseClicked
    private static final String VOICENAME = "kevin16";

    private void speaker(JTextArea textArea) {
        VoiceManager voiceManager = VoiceManager.getInstance();
        Voice voice = voiceManager.getVoice(VOICENAME);
        voice.allocate();
        try {
            voice.speak(textArea.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "The problem occured");
        }
    }
