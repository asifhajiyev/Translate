    private void buttonTranslateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTranslateActionPerformed

        if (label1.getText().equals("Azerbaijani")) {
            list.stream().filter((w) -> (w.getWordAZ().toLowerCase().equalsIgnoreCase(textArea1.getText().trim().toLowerCase()))).forEachOrdered((w) -> {
                if (!textArea2.getText().contains(w.getWordEN().toLowerCase())) {
                    textArea2.append(w.getWordEN().toLowerCase() + "\n");
                }
            });
            if (textArea1.getText().equals("")) {
                textArea2.setText("");
            }
        } else {
            list.stream().filter((w) -> (w.getWordEN().toLowerCase().equalsIgnoreCase(textArea1.getText().trim().toLowerCase()))).forEachOrdered((w) -> {
                if (!textArea2.getText().contains(w.getWordAZ().trim().toLowerCase())) {
                    textArea2.append(w.getWordAZ().toLowerCase() + "\n");
                }
            });
            if (textArea1.getText().equals("")) {
                textArea2.setText("");
            }
        }
    }//GEN-LAST:event_buttonTranslateActionPerformed

